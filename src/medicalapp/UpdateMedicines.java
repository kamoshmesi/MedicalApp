    public ArrayList<Medicines> medicineList()
    {
        ArrayList<Medicines> medicineList = new ArrayList<>();
        
        String sql = "select * from tbl_drugs where DName = ? ";
        
        try{
            pst = Conn.prepareStatement(sql);
           pst.setString(1, txt_Dname.getText());
           rs = pst.executeQuery();
            Medicines medicines;
            while(rs.next())
            {
                medicines = new Medicines(rs.getInt("Dno"),rs.getString("Dname"),rs.getString("cutOff"),rs.getInt("Qty"),rs.getString("ARange"),rs.getInt("Amount"),rs.getString("Date"));
                medicineList.add(medicines);
            }
            
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        
        return medicineList;
    }
    
    public void showMedicines()
    {
        ArrayList<Medicines> list = medicineList();
        DefaultTableModel model = (DefaultTableModel)tbl_medi.getModel();
        Object[] row = new Object[7];
        for(int i= 0;i<list.size();i++)
        {
            row[0] = list.get(i).getDno();
            row[1] = list.get(i).getDname();
            row[2] = list.get(i).getCutOff();
            row[3] = list.get(i).getQty();
            row[4] = list.get(i).getARange();
            row[5] = list.get(i).getAmount();
            row[6] = list.get(i).getDate();
            model.addRow(row);
        }
        
        
    }

