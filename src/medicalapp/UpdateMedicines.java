    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        
        if(txt_DNo.getText().trim().isEmpty() && txt_Dname.getText().trim().isEmpty() && txt_ageRange.getText().trim().isEmpty() && txt_amount.getText().trim().isEmpty() && txt_cutOff.getText().trim().isEmpty() && txt_date.getText().trim().isEmpty() && txt_qty.getText().trim().isEmpty()   )
        {
            JOptionPane.showMessageDialog(null,"Plaese Enter the data");
        }
        else if(txt_DNo.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Plaese Select Drugs from table");
        }
        else if(txt_Dname.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Plaese Enter the name");
        }
        else if(txt_ageRange.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Plaese Enter the Age range");
        }
        else if(txt_amount.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Plaese Enter the Available Amount");
        }
        else if(txt_cutOff.getText().trim().isEmpty())
        {
            JOptionPane.showMessageDialog(null,"Plaese Enter the cut-off value");
        }
        else{
            
        
        
        String sql = "update tbl_drugs set cutOff= '"+txt_cutOff.getText()+"', Qty='"+txt_qty.getText()+"', ARange='"+txt_ageRange.getText()+"', Amount ='"+txt_amount.getText()+"', Date='"+date+"' where DNo='"+txt_DNo.getText()+"'";
        
        try{
            st = Conn.createStatement();
            if((st.executeUpdate(sql)) ==1 )
            {
                JOptionPane.showMessageDialog(null,"Data Updated Succefully");
                DefaultTableModel model = (DefaultTableModel)tbl_medi.getModel();
                model.setRowCount(0);
                showMedicines();
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Data not updated Succefully");
            }
            
            
            
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        }
    }//GEN-LAST:event_btn_updateActionPerformed

